using Content.Client.Audio;
using Content.Client._Sunrise.Options.UI;
using Content.Shared._Sunrise.Lobby;
using Content.Shared._Sunrise.SunriseCCVars;
using Content.Shared.GameTicking;
using System.Linq;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Configuration;
using Robust.Shared.Prototypes;

namespace Content.Client.Options.UI.Tabs;

[GenerateTypedNameReferences]
public sealed partial class ExtraTab : Control
{
    // Sunrise added start - centralize the shared Random option token
    private const string LobbyBackgroundRandom = "Random";
    // Sunrise added end

    [Dependency] private readonly IConfigurationManager _cfg = default!;
    [Dependency] private readonly IPrototypeManager _prototype = default!;
    [Dependency] private readonly ILogManager _log = default!;

    private readonly ISawmill _sawmill;

    private readonly SunriseOptionDropDownCVar<string> _lobbyBackgroundTypeOption;
    private readonly SunriseOptionDropDownCVar<string> _lobbyArtOption;
    private readonly SunriseOptionDropDownCVar<string> _lobbyAnimationOption;
    private readonly SunriseOptionDropDownCVar<string> _lobbyParallaxOption;

    public ExtraTab()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _sawmill = _log.GetSawmill("options.extra");

        Control.AddOptionPercentSlider(
            SunriseCCVars.TTSVolume,
            SliderTts,
            scale: ContentAudioSystem.TtsMultiplier);

        Control.AddOptionPercentSlider(
            SunriseCCVars.TTSRadioVolume,
            SliderTtsRadio,
            scale: ContentAudioSystem.TtsMultiplier);

        Control.AddOptionCheckBox(SunriseCCVars.TTSClientEnabled, TtsClientCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.TTSClientQueueEnabled, TtsClientCheckBoxQueue);
        Control.AddOptionCheckBox(SunriseCCVars.TTSRadioGhostEnabled, TtsRadioGhostCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.TapePlayerClientEnabled, TapePlayerClientCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.JumpSoundDisable, JumpSoundDisableCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.VoteMusicDisable, VoteMusicDisableCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.MuteGhostRoleNotification, MuteGhostRoleNotificationCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.TracesEnabled, TracesCheckBox);

        Control.AddOptionCheckBox(SunriseCCVars.PlayHeartBeatSound, PlayHeartbeatSound);

        var lobbyBackgroundOptions = GetLobbyBackgroundOptions();

        _lobbyBackgroundTypeOption = Control.AddOption(new SunriseOptionDropDownCVar<string>(
            Control,
            _cfg,
            SunriseCCVars.LobbyBackgroundType,
            DropDownLobbyBackgroundType,
            BuildLobbyBackgroundTypeOptions(lobbyBackgroundOptions.BackgroundTypes)));
        _lobbyArtOption = Control.AddOption(new SunriseOptionDropDownCVar<string>(
            Control,
            _cfg,
            SunriseCCVars.LobbyArt,
            DropDownLobbyArt,
            BuildLobbyArtOptions(lobbyBackgroundOptions.Arts)));
        _lobbyAnimationOption = Control.AddOption(new SunriseOptionDropDownCVar<string>(
            Control,
            _cfg,
            SunriseCCVars.LobbyAnimation,
            DropDownLobbyAnimation,
            BuildLobbyAnimationOptions(lobbyBackgroundOptions.Animations)));
        _lobbyParallaxOption = Control.AddOption(new SunriseOptionDropDownCVar<string>(
            Control,
            _cfg,
            SunriseCCVars.LobbyParallax,
            DropDownLobbyParallax,
            BuildLobbyParallaxOptions(lobbyBackgroundOptions.Parallaxes)));

        _cfg.OnValueChanged(SunriseCCVars.LobbyBackgroundType, OnLobbyBackgroundTypeChanged, true);
        _cfg.OnValueChanged(SunriseCCVars.LobbyBackgroundPreset, OnLobbyBackgroundPresetChanged, true);

        Control.AddOptionPercentSlider(SunriseCCVars.LobbyOpacity, LobbyOpacitySlider);
        Control.AddOptionCheckBox(SunriseCCVars.LobbyUnloadResources, LobbyUnloadResourcesCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.DamageOverlayEnable, DamageOverlayEnableCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.DamageOverlaySelf, DamageOverlaySelfCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.DamageOverlayStructures, DamageOverlayStructuresCheckBox);

        Control.AddOptionCheckBox(SunriseCCVars.ChatIconsEnable, ChatIconsEnableCheckBox);
        Control.AddOptionCheckBox(SunriseCCVars.ChatPointingVisuals, ChatPointingVisualsEnableCheckBox);

        Control.Initialize();
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();
        _cfg.UnsubValueChanged(SunriseCCVars.LobbyBackgroundType, OnLobbyBackgroundTypeChanged);
        _cfg.UnsubValueChanged(SunriseCCVars.LobbyBackgroundPreset, OnLobbyBackgroundPresetChanged);
    }

    private void OnLobbyBackgroundPresetChanged(string presetId)
    {
        RefreshLobbyBackgroundOptions();
    }

    private void OnLobbyBackgroundTypeChanged(string lobbyBackgroundTypeString)
    {
        var allowedTypes = GetAllowedLobbyBackgroundTypes();
        if (lobbyBackgroundTypeString == LobbyBackgroundRandom)
        {
            DropDownLobbyArt.Visible = allowedTypes.Contains(LobbyBackgroundType.Art);
            DropDownLobbyAnimation.Visible = allowedTypes.Contains(LobbyBackgroundType.Animation);
            DropDownLobbyParallax.Visible = allowedTypes.Contains(LobbyBackgroundType.Parallax);
            return;
        }

        var lobbyBackgroundType = ResolveAllowedLobbyBackgroundType(lobbyBackgroundTypeString, allowedTypes);
        DropDownLobbyArt.Visible = lobbyBackgroundType == LobbyBackgroundType.Art;
        DropDownLobbyAnimation.Visible = lobbyBackgroundType == LobbyBackgroundType.Animation;
        DropDownLobbyParallax.Visible = lobbyBackgroundType == LobbyBackgroundType.Parallax;
    }

    private void RefreshLobbyBackgroundOptions()
    {
        var lobbyBackgroundOptions = GetLobbyBackgroundOptions();

        _lobbyBackgroundTypeOption.ReplaceOptions(BuildLobbyBackgroundTypeOptions(lobbyBackgroundOptions.BackgroundTypes));
        _lobbyArtOption.ReplaceOptions(BuildLobbyArtOptions(lobbyBackgroundOptions.Arts));
        _lobbyAnimationOption.ReplaceOptions(BuildLobbyAnimationOptions(lobbyBackgroundOptions.Animations));
        _lobbyParallaxOption.ReplaceOptions(BuildLobbyParallaxOptions(lobbyBackgroundOptions.Parallaxes));

        // Sunrise edit start - use the current dropdown selection after replacing options
        OnLobbyBackgroundTypeChanged((string?) DropDownLobbyBackgroundType.Button.SelectedMetadata ?? LobbyBackgroundRandom);
        // Sunrise edit end
        Control.RefreshButtonState();
    }

    private LobbyBackgroundType ResolveAllowedLobbyBackgroundType(
        string configuredType,
        IReadOnlyList<LobbyBackgroundType> allowedTypes)
    {
        if (Enum.TryParse<LobbyBackgroundType>(configuredType, true, out var resolvedType) &&
            allowedTypes.Contains(resolvedType))
        {
            return resolvedType;
        }

        if (allowedTypes.Count > 0)
            return allowedTypes[0];

        return LobbyBackgroundType.Parallax;
    }

    private List<SunriseOptionDropDownCVar<string>.ValueOption> BuildLobbyBackgroundTypeOptions(
        IReadOnlyList<LobbyBackgroundType> allowedTypes)
    {
        var options = new List<SunriseOptionDropDownCVar<string>.ValueOption>
        {
            new(LobbyBackgroundRandom, Loc.GetString("background-type-Random")),
        };

        foreach (var backgroundType in allowedTypes)
        {
            var label = Loc.GetString($"background-type-{backgroundType}");
            options.Add(new SunriseOptionDropDownCVar<string>.ValueOption(backgroundType.ToString(), label));
        }

        return options;
    }

    private List<SunriseOptionDropDownCVar<string>.ValueOption> BuildLobbyArtOptions(
        IReadOnlyList<LobbyArtPrototype> allowedArts)
    {
        var options = new List<SunriseOptionDropDownCVar<string>.ValueOption>
        {
            new(LobbyBackgroundRandom, Loc.GetString("lobby-art-Random")),
        };

        foreach (var lobbyArt in allowedArts)
        {
            var label = Loc.GetString($"lobby-art-{lobbyArt.ID}");
            options.Add(new SunriseOptionDropDownCVar<string>.ValueOption(lobbyArt.ID, label));
        }

        return options;
    }

    private List<SunriseOptionDropDownCVar<string>.ValueOption> BuildLobbyAnimationOptions(
        IReadOnlyList<LobbyAnimationPrototype> allowedAnimations)
    {
        var options = new List<SunriseOptionDropDownCVar<string>.ValueOption>
        {
            new(LobbyBackgroundRandom, Loc.GetString("lobby-animation-Random")),
        };

        foreach (var lobbyAnimation in allowedAnimations)
        {
            var label = Loc.GetString($"lobby-animation-{lobbyAnimation.ID}");
            options.Add(new SunriseOptionDropDownCVar<string>.ValueOption(lobbyAnimation.ID, label));
        }

        return options;
    }

    private List<SunriseOptionDropDownCVar<string>.ValueOption> BuildLobbyParallaxOptions(
        IReadOnlyList<LobbyParallaxPrototype> allowedParallaxes)
    {
        var options = new List<SunriseOptionDropDownCVar<string>.ValueOption>
        {
            new(LobbyBackgroundRandom, Loc.GetString("lobby-parallax-Random")),
        };

        foreach (var lobbyParallax in allowedParallaxes)
        {
            var label = Loc.GetString($"lobby-parallax-{lobbyParallax.ID}");
            options.Add(new SunriseOptionDropDownCVar<string>.ValueOption(lobbyParallax.ID, label));
        }

        return options;
    }

    private IReadOnlyList<LobbyBackgroundType> GetAllowedLobbyBackgroundTypes()
    {
        var preset = GetCurrentLobbyBackgroundPreset();
        return GetAllowedLobbyBackgroundTypes(
            GetAllowedLobbyArts(preset),
            GetAllowedLobbyAnimations(preset),
            GetAllowedLobbyParallaxes(preset));
    }

    private (
        IReadOnlyList<LobbyBackgroundType> BackgroundTypes,
        IReadOnlyList<LobbyArtPrototype> Arts,
        IReadOnlyList<LobbyAnimationPrototype> Animations,
        IReadOnlyList<LobbyParallaxPrototype> Parallaxes)
        GetLobbyBackgroundOptions()
    {
        var preset = GetCurrentLobbyBackgroundPreset();
        var allowedArts = GetAllowedLobbyArts(preset);
        var allowedAnimations = GetAllowedLobbyAnimations(preset);
        var allowedParallaxes = GetAllowedLobbyParallaxes(preset);

        return (
            GetAllowedLobbyBackgroundTypes(allowedArts, allowedAnimations, allowedParallaxes),
            allowedArts,
            allowedAnimations,
            allowedParallaxes);
    }

    private List<LobbyBackgroundType> GetAllowedLobbyBackgroundTypes(
        List<LobbyArtPrototype> allowedArts,
        List<LobbyAnimationPrototype> allowedAnimations,
        List<LobbyParallaxPrototype> allowedParallaxes)
    {
        var availableTypes = new List<LobbyBackgroundType>();

        if (allowedAnimations.Count > 0)
            availableTypes.Add(LobbyBackgroundType.Animation);

        if (allowedParallaxes.Count > 0)
            availableTypes.Add(LobbyBackgroundType.Parallax);

        if (allowedArts.Count > 0)
            availableTypes.Add(LobbyBackgroundType.Art);

        if (availableTypes.Count > 0)
            return availableTypes;

        var fallbackType = Enum.GetValues<LobbyBackgroundType>().First();
        _sawmill.Warning(
            $"No lobby background asset types are allowed for the current preset; falling back to {fallbackType}.");
        return [fallbackType];
    }

    private List<LobbyArtPrototype> GetAllowedLobbyArts(LobbyBackgroundPresetPrototype? preset)
    {
        return _prototype.EnumeratePrototypes<LobbyArtPrototype>()
            .Where(x => preset == null || preset.AllArtsAllowed || preset.WhitelistArts.Contains(x.ID))
            .ToList();
    }

    private List<LobbyAnimationPrototype> GetAllowedLobbyAnimations(LobbyBackgroundPresetPrototype? preset)
    {
        return _prototype.EnumeratePrototypes<LobbyAnimationPrototype>()
            .Where(x => preset == null || preset.AllAnimationsAllowed || preset.WhitelistAnimations.Contains(x.ID))
            .ToList();
    }

    private List<LobbyParallaxPrototype> GetAllowedLobbyParallaxes(LobbyBackgroundPresetPrototype? preset)
    {
        return _prototype.EnumeratePrototypes<LobbyParallaxPrototype>()
            .Where(x => preset == null || preset.AllParallaxesAllowed || preset.WhitelistParallaxes.Contains(x.ID))
            .ToList();
    }

    private LobbyBackgroundPresetPrototype? GetCurrentLobbyBackgroundPreset()
    {
        var presetId = _cfg.GetCVar(SunriseCCVars.LobbyBackgroundPreset);
        if (_prototype.TryIndex<LobbyBackgroundPresetPrototype>(presetId, out var preset))
            return preset;

        return null;
    }
}
