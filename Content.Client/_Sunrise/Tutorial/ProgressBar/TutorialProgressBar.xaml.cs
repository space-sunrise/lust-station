using System;
using System.Numerics;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Sunrise.Tutorial.ProgressBar;

[GenerateTypedNameReferences]
public sealed partial class TutorialProgressBar : PanelContainer
{
    public TutorialProgressBar()
    {
        RobustXamlLoader.Load(this);
    }

    public void SetLabel(string text)
    {
        ProgressText.Text = text;
    }

    public void SetProgress(float value)
    {
        Progress.Value = value;
    }

    public void SetControlWidth(float width)
    {
        var clamped = MathF.Max(width, 120f);
        MinSize = new Vector2(clamped, 0f);
        Progress.MinSize = new Vector2(clamped, Progress.MinSize.Y);
    }
}
