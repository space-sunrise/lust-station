using System;
using Content.Client.Message;
using Content.Shared._Sunrise.Roadmap;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Sunrise.Roadmap;

[GenerateTypedNameReferences]
public sealed partial class RoadmapItem : Control
{
    private RoadmapItemState _itemState;
    private float _lastSetWidth;

    public event Action<string>? OnLikePressed;

    public string GoalId { get; set; } = string.Empty;

    public RoadmapItemState ItemState
    {
        get => _itemState;
        set
        {
            _itemState = value;
            var panel = (StyleBoxFlat) StateColor.PanelOverride!;
            panel.BackgroundColor = RoadmapColorPallet.GetStateColor(_itemState);

            StateText.Text = _itemState switch
            {
                RoadmapItemState.Planned => Loc.GetString("ui-roadmap-state-planned"),
                RoadmapItemState.InProgress => Loc.GetString("ui-roadmap-state-in-progress"),
                RoadmapItemState.Partial => Loc.GetString("ui-roadmap-state-partial"),
                RoadmapItemState.Complete => Loc.GetString("ui-roadmap-state-complete"),
                _ => string.Empty,
            };
            StateText.ModulateSelfOverride = panel.BackgroundColor;
        }
    }

    public string? HeaderText
    {
        get => HeaderLabel.Text;
        set => HeaderLabel.Text = value;
    }

    public string? Text
    {
        get => Contents.GetMessage();
        set => Contents.SetMarkup($"[font size=12]{value ?? string.Empty}[/font]");
    }

    public int LikeCount
    {
        get => LikesControl.LikeCount;
        set => LikesControl.LikeCount = value;
    }

    public bool LikedByPlayer
    {
        get => LikesControl.LikedByPlayer;
        set => LikesControl.LikedByPlayer = value;
    }

    public RoadmapItem()
    {
        RobustXamlLoader.Load(this);

        HeaderButton.OnPressed += _ => ContentsContainer.Visible = !ContentsContainer.Visible;
        LikesControl.OnLikePressed += HandleLikePressed;

        OnResized += UpdateContentsWidth;
        LikeCount = 0;
        LikedByPlayer = false;
    }

    private void HandleLikePressed()
    {
        if (string.IsNullOrWhiteSpace(GoalId))
            return;

        OnLikePressed?.Invoke(GoalId);
    }

    private void UpdateContentsWidth()
    {
        // GridContainer measures children with the full available size, not the per-column size.
        // So RichTextLabel gets infinite width during measure and never wraps.
        // We fix this by explicitly setting its width to the actual arranged width of this control.
        var targetWidth = ContentsContainer.Width - Contents.Margin.Left - Contents.Margin.Right;
        if (targetWidth <= 0 || MathF.Abs(_lastSetWidth - targetWidth) < 0.5f)
            return;

        _lastSetWidth = targetWidth;
        Contents.SetWidth = targetWidth;
        InvalidateMeasure();
    }
}
