using Content.Client._Sunrise.Sandbox;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Sunrise.UserInterface.Systems.Sandbox.Widgets;

/// <summary>
/// Widget that exposes mapping access overlay filters to the user.
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class MappingAccessWidget : UIWidget
{
    /// <summary>
    /// Raised after the selected body filter changes.
    /// </summary>
    public event Action<MappingAccessBodyFilter>? FilterChanged;

    /// <summary>
    /// Raised after the electronics-only toggle changes.
    /// </summary>
    public event Action<bool>? ElectronicsOnlyChanged;

    /// <summary>
    /// Creates the widget and initializes its filter controls.
    /// </summary>
    public MappingAccessWidget()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        Visible = false;

        TitleLabel.FontColorOverride = Color.Aquamarine;

        AddFilterOption(MappingAccessBodyFilter.Both, "mapping-access-widget-filter-both");
        AddFilterOption(MappingAccessBodyFilter.Static, "mapping-access-widget-filter-static");
        AddFilterOption(MappingAccessBodyFilter.Dynamic, "mapping-access-widget-filter-dynamic");
        ElectronicsOnlyCheckBox.OnToggled += args => ElectronicsOnlyChanged?.Invoke(args.Pressed);
        BodyFilterOptionButton.OnItemSelected += OnBodyFilterSelected;
        BodyFilterOptionButton.SelectId((int) MappingAccessBodyFilter.Both);
    }

    /// <summary>
    /// Updates the selected body filter without raising widget events.
    /// </summary>
    public void SetBodyFilter(MappingAccessBodyFilter filter)
    {
        BodyFilterOptionButton.SelectId((int) filter);
    }

    /// <summary>
    /// Updates the electronics-only toggle without raising widget events.
    /// </summary>
    public void SetElectronicsOnly(bool electronicsOnly)
    {
        ElectronicsOnlyCheckBox.Pressed = electronicsOnly;
    }

    private void AddFilterOption(MappingAccessBodyFilter filter, string locKey)
    {
        BodyFilterOptionButton.AddItem(Loc.GetString(locKey), (int) filter);
        BodyFilterOptionButton.SetItemMetadata(BodyFilterOptionButton.GetIdx((int) filter), filter);
    }

    private void OnBodyFilterSelected(OptionButton.ItemSelectedEventArgs args)
    {
        args.Button.SelectId(args.Id);

        if (args.Button.SelectedMetadata is MappingAccessBodyFilter filter)
            FilterChanged?.Invoke(filter);
    }
}
