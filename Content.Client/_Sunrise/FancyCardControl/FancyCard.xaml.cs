using System.Numerics;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.ResourceManagement;
using Robust.Shared.Utility;
using Content.Client.Stylesheets;

namespace Content.Client._Sunrise.FancyCardControl;

[GenerateTypedNameReferences]
public sealed partial class FancyCard : Control
{
    [Dependency] private readonly IResourceCache _resourceCache = default!;
    private string _descText = string.Empty;
    private Vector2 _defaultButtonSize = new(120, 30);
    public FancyCard(FancyCardConfig config)
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);
        ApplyConfig(config);
    }

    public void ApplyConfig(FancyCardConfig config)
    {
        TitleLabel.Text = Loc.GetString(config.TitleText);

        _descText = config.DescText;

        var formattedMessage = new FormattedMessage();
        formattedMessage.AddMarkupOrThrow(Loc.GetString(string.IsNullOrEmpty(_descText) ? "-" : _descText));

        DescLabel.SetMessage(formattedMessage);
        DescOuter.Visible = config.ShowDescription;

        if (!string.IsNullOrEmpty(config.BackdropTexture.CanonPath))
            Backdrop.Texture = _resourceCache.GetResource<TextureResource>(config.BackdropTexture);

        if (config.CardSize != null)
            SetSize = config.CardSize.Value;

        ApplyActions(config);
    }

    public void SetDenied(bool denied)
    {
        foreach (var child in ActionButtons.Children)
        {
            if (child is Button button)
                button.Disabled = denied;
        }
    }

    private void ApplyActions(FancyCardConfig config)
    {
        ActionButtons.DisposeAllChildren();

        if (config.Buttons == null)
        {
            ActionPanel.Visible = false;
            return;
        }

        var actions = config.Buttons;

        foreach (var action in actions)
        {
            var button = new Button
            {
                Text = action.Text,
                Disabled = action.Disabled
            };

            if (!string.IsNullOrEmpty(action.Tooltip))
                button.ToolTip = action.Tooltip;

            button.StyleClasses.Add(StyleClass.ButtonSquare);
            button.SetSize = action.ButtonSize == null ? _defaultButtonSize : action.ButtonSize.Value;
            button.Margin = new Thickness(0, 7, 0, 7);

            button.OnPressed += _ => action.OnPressed?.Invoke();

            ActionButtons.AddChild(button);
        }

        ActionPanel.Visible = actions.Count > 0;
    }
}

public sealed record FancyCardConfig
{
    public string TitleText { get; init; } = string.Empty;
    public string DescText { get; init; } = string.Empty;
    public string? ActionTooltip { get; init; }
    public IReadOnlyList<FancyCardButton>? Buttons { get; init; }
    public ResPath BackdropTexture { get; init; }
    public Vector2? CardSize { get; init; }
    public bool ShowDescription { get; init; } = true;
}

public sealed record FancyCardButton(
    string? Id,
    string Text,
    Vector2? ButtonSize = null,
    string? Tooltip = null,
    bool Disabled = false,
    Action? OnPressed = null);
