using Content.Shared.Radio;
using Content.Shared.Radio.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Client.UserInterface.Controls;
using Robust.Client.Graphics;
using Content.Shared._Sunrise.Radio;

namespace Content.Client._Sunrise.Radio.Ui;

[GenerateTypedNameReferences]
public sealed partial class HeadsetSettingsWindow : DefaultWindow
{
    public Action<string, bool>? OnChannelToggled;
    public Action<string, float>? OnVolumeChanged;

    public HeadsetSettingsWindow()
    {
        RobustXamlLoader.Load(this);
    }

    public void UpdateState(HeadsetComponent component, EncryptionKeyHolderComponent keys, IPrototypeManager proto)
    {
        ChannelList.DisposeAllChildren();


        foreach (var channelId in keys.Channels)
        {
            if (!proto.TryIndex(channelId, out var channel))
                continue;

            // Sunrise-Start: Style each channel row using its custom channel color
            var row = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                SeparationOverride = 8,
                HorizontalExpand = true,
                Margin = new Thickness(8, 6)
            };

            var panel = new PanelContainer
            {
                PanelOverride = new StyleBoxFlat
                {
                    BackgroundColor = channel.Color.WithAlpha(0.08f),
                    BorderColor = channel.Color.WithAlpha(0.18f),
                    BorderThickness = new Thickness(1)
                },
                Margin = new Thickness(0, 4),
                HorizontalExpand = true
            };
            panel.AddChild(row);
            // Sunrise-End

            var enabled = component.EnabledChannels.GetValueOrDefault(channelId, true);

            var checkbox = new CheckBox
            {
                Pressed = enabled,
                VerticalAlignment = VAlignment.Center
            };

            var label = new Label
            {
                Text = channel.LocalizedName,
                MinWidth = 120,
                HorizontalExpand = true,
                VerticalAlignment = VAlignment.Center,
                ClipText = true
            };

            var leftPart = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                SeparationOverride = 4,
                MinWidth = 160
            };
            leftPart.AddChild(checkbox);
            leftPart.AddChild(label);

            row.AddChild(leftPart);

            var volume = component.ChannelVolumes.GetValueOrDefault(channelId, 1.0f);
            var slider = new Slider
            {
                MinValue = 0f,
                MaxValue = 1f,
                Value = volume,
                HorizontalExpand = true,
                Visible = enabled,
                VerticalAlignment = VAlignment.Center
            };
            slider.OnValueChanged += args => OnVolumeChanged?.Invoke(channelId, args.Value);
            checkbox.OnToggled += args =>
            {
                OnChannelToggled?.Invoke(channelId, args.Pressed);
                slider.Visible = args.Pressed;
            };
            row.AddChild(slider);

            ChannelList.AddChild(panel);
        }
    }
}
