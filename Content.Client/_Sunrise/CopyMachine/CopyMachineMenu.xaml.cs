using System.Linq;
using Content.Client._Sunrise.UserInterface;
using Content.Shared._Sunrise.CopyMachine;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;

namespace Content.Client._Sunrise.CopyMachine;

[GenerateTypedNameReferences]
public sealed partial class CopyMachineMenu : DefaultWindow
{
    [Dependency] private readonly IEntitySystemManager _entitySystems = default!;
    [Dependency] private readonly ILocalizationManager _loc = default!;
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    public event Action<string?>? OnPrintPressed;
    public event Action? OnCopyPressed;
    public event Action<int>? OnCancelQueueJobPressed;

    private const int MaxListLabelLength = 64;
    private const float ListIconTargetSize = 40f;
    private const float ListIconScaleMin = 0.1f;
    private const float ListIconScaleMax = 2.5f;

    private static readonly ResPath CopyMachineActionsRsiPath = new("/Textures/_Sunrise/Interface/CopyMachine/copy_machine_actions.rsi");
    private const string PrintIconState = "print";
    private const string CopyIconState = "copy";

    private string? _currentTemplateId;
    private bool _canCopyFromCurrentSource;
    private float _inkVolume;
    private string _searchText = string.Empty;
    private List<string> _allTemplateIds = new();
    private ProtoId<DocTemplateCategoryPrototype>? _activeCategoryFilterId;
    private bool _suppressCategorySelection;

    private readonly Dictionary<string, Texture> _categoryIconTextures = new();
    private readonly List<ProtoId<DocTemplateCategoryPrototype>?> _categoryFilterIdsByOptionId = new();

    private Texture? _printIconTexture;
    private Texture? _copyIconTexture;

    private SpriteSystem SpriteSystem => _entitySystems.GetEntitySystem<SpriteSystem>();

    public CopyMachineMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        PrintButton.OnPressed += _ => OnPrintPressed?.Invoke(_currentTemplateId);
        CopyButton.OnPressed += _ => OnCopyPressed?.Invoke();

        PrintIcon.Texture = GetPrintIconTexture();
        CopyIcon.Texture = GetCopyIconTexture();

        TemplateList.OnItemSelected += OnTemplateSelected;
        TemplateList.OnItemDeselected += OnTemplateDeselected;

        SearchBar.OnTextChanged += _ =>
        {
            _searchText = SearchBar.Text.Trim();
            PopulateTemplates();
        };

        CategoryDropdown.OnItemSelected += OnCategorySelected;
        RebuildCategoryDropdown();
    }

    public void UpdateState(CopyMachineBoundUserInterfaceState state)
    {
        var templatesChanged = !TemplatesEqual(_allTemplateIds, state.Templates);

        _canCopyFromCurrentSource = state.CanCopy;
        _inkVolume = state.InkAmount;

        if (templatesChanged)
            _allTemplateIds = state.Templates;

        UpdateStats(state);
        UpdateButtons();
        UpdateCurrentJob(state);
        UpdateQueue(state);

        if (!templatesChanged)
            return;

        RebuildCategoryDropdown();
        PopulateTemplates();
    }

    private void OnTemplateSelected(ItemList.ItemListSelectedEventArgs args)
    {
        _currentTemplateId = args.ItemList[args.ItemIndex].Metadata as string;
        PrintButton.Disabled = _inkVolume <= 0.0f || _currentTemplateId == null;
    }

    private void OnTemplateDeselected(ItemList.ItemListDeselectedEventArgs args)
    {
        _currentTemplateId = null;
        PrintButton.Disabled = true;
    }

    private void UpdateStats(CopyMachineBoundUserInterfaceState state)
    {
        PaperCountLabel.Text = state.PaperCount.ToString();
        InkAmountLabel.Text = _inkVolume.ToString("F1");

        var copyStatusText = _canCopyFromCurrentSource
            ? _loc.GetString("copy-machine-menu-copy-available")
            : _loc.GetString("copy-machine-menu-copy-unavailable");

        CopyButton.ToolTip = $"{_loc.GetString("copy-machine-menu-copy")}\n{copyStatusText}";
    }

    private void UpdateButtons()
    {
        PrintButton.Disabled = _currentTemplateId == null || _inkVolume <= 0.0f;
        CopyButton.Disabled = !_canCopyFromCurrentSource;
    }

    private void UpdateCurrentJob(CopyMachineBoundUserInterfaceState state)
    {
        var currentJobText = state.CurrentJob?.ToString() ?? _loc.GetString("copy-machine-menu-no-active-job");
        CurrentJobLabel.Text = UiTextHelper.TruncateWithEllipsis(currentJobText, MaxListLabelLength);
        CurrentJobLabel.ToolTip = currentJobText;
    }

    private void UpdateQueue(CopyMachineBoundUserInterfaceState state)
    {
        QueueContainer.RemoveAllChildren();

        for (var i = 0; i < state.Queue.Count; i++)
        {
            var job = state.Queue[i];
            var fullText = job.ToString();
            var icon = job.Type switch
            {
                CopyMachineJobType.Print => GetPrintIconTexture(),
                CopyMachineJobType.Copy => GetCopyIconTexture(),
                _ => null,
            };

            var row = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                HorizontalExpand = true,
                Margin = new Thickness(2, 1),
                SeparationOverride = 4,
            };

            if (icon != null)
            {
                row.AddChild(new TextureRect
                {
                    Texture = icon,
                    SetSize = new(16, 16),
                    Stretch = TextureRect.StretchMode.KeepAspectCentered,
                    VerticalAlignment = VAlignment.Center,
                    CanShrink = true,
                });
            }

            row.AddChild(new Label
            {
                Text = UiTextHelper.TruncateWithEllipsis(fullText, MaxListLabelLength),
                HorizontalExpand = true,
                ClipText = true,
                ToolTip = fullText,
            });

            var cancelButton = new TextureButton();
            cancelButton.AddStyleClass("windowCloseButton");
            cancelButton.ToolTip = _loc.GetString("copy-machine-menu-cancel-job");

            var index = i;
            cancelButton.OnPressed += _ => OnCancelQueueJobPressed?.Invoke(index);

            row.AddChild(cancelButton);
            QueueContainer.AddChild(row);
        }
    }

    private static bool TemplatesEqual(List<string> a, List<string> b)
    {
        if (ReferenceEquals(a, b))
            return true;

        if (a.Count != b.Count)
            return false;

        for (var i = 0; i < a.Count; i++)
        {
            if (a[i] != b[i])
                return false;
        }

        return true;
    }

    private void RebuildCategoryDropdown()
    {
        _suppressCategorySelection = true;
        CategoryDropdown.Clear();
        _categoryFilterIdsByOptionId.Clear();

        CategoryDropdown.AddItem(_loc.GetString("copy-machine-filter-all"), 0);
        _categoryFilterIdsByOptionId.Add(null);

        var countsByCategory = GetUsedCategoryCounts();
        foreach (var category in GetOrderedCategories(countsByCategory.Keys))
        {
            var categoryId = new ProtoId<DocTemplateCategoryPrototype>(category.ID);
            if (!countsByCategory.TryGetValue(categoryId, out var count) || count <= 0)
                continue;

            var localized = _loc.GetString(category.Name);
            CategoryDropdown.AddItem($"{localized} ({count})", _categoryFilterIdsByOptionId.Count);
            _categoryFilterIdsByOptionId.Add(categoryId);
        }

        var selectedId = 0;
        if (_activeCategoryFilterId != null)
            selectedId = _categoryFilterIdsByOptionId.IndexOf(_activeCategoryFilterId);

        if (selectedId <= 0)
        {
            _activeCategoryFilterId = null;
            selectedId = 0;
        }

        CategoryDropdown.SelectId(selectedId);
        _suppressCategorySelection = false;
    }

    private Dictionary<ProtoId<DocTemplateCategoryPrototype>, int> GetUsedCategoryCounts()
    {
        var counts = new Dictionary<ProtoId<DocTemplateCategoryPrototype>, int>();

        foreach (var templateId in _allTemplateIds)
        {
            if (!_prototypeManager.TryIndex<DocTemplatePrototype>(templateId, out var template))
                continue;

            counts.TryAdd(template.Category, 0);
            counts[template.Category]++;
        }

        return counts;
    }

    private void OnCategorySelected(OptionButton.ItemSelectedEventArgs args)
    {
        if (_suppressCategorySelection)
            return;

        if (args.Id < 0 || args.Id >= _categoryFilterIdsByOptionId.Count)
            return;

        SetActiveCategoryFilter(_categoryFilterIdsByOptionId[args.Id]);
        args.Button.SelectId(args.Id);
    }

    private void SetActiveCategoryFilter(ProtoId<DocTemplateCategoryPrototype>? categoryId)
    {
        _activeCategoryFilterId = categoryId;
        PopulateTemplates();
    }

    private void PopulateTemplates()
    {
        var previouslySelected = _currentTemplateId;
        _currentTemplateId = null;
        TemplateList.Clear();

        var filteredTemplateIds = _allTemplateIds.Where(MatchesTemplateFilter).ToList();
        var shouldGroupByCategory = _activeCategoryFilterId == null && string.IsNullOrWhiteSpace(_searchText);

        if (shouldGroupByCategory)
        {
            var templatesByCategory = new Dictionary<ProtoId<DocTemplateCategoryPrototype>, List<TemplateEntry>>();
            foreach (var templateId in filteredTemplateIds)
            {
                if (!TryGetTemplateEntry(templateId, out var entry))
                    continue;

                if (!templatesByCategory.TryGetValue(entry.Template.Category, out var entries))
                {
                    entries = new List<TemplateEntry>();
                    templatesByCategory[entry.Template.Category] = entries;
                }

                entries.Add(entry);
            }

            foreach (var category in GetOrderedCategories(templatesByCategory.Keys))
            {
                var categoryId = new ProtoId<DocTemplateCategoryPrototype>(category.ID);
                if (!templatesByCategory.TryGetValue(categoryId, out var entries) || entries.Count == 0)
                    continue;

                AddCategoryHeader(category, entries.Count);

                entries.Sort(static (a, b) => string.Compare(a.FullName, b.FullName, StringComparison.CurrentCulture));
                foreach (var entry in entries)
                {
                    AddTemplateItem(entry.TemplateId, entry.Template, entry.FullName);
                }
            }
        }
        else
        {
            foreach (var templateId in filteredTemplateIds)
            {
                if (!TryGetTemplateEntry(templateId, out var entry))
                    continue;

                AddTemplateItem(entry.TemplateId, entry.Template, entry.FullName);
            }
        }

        if (previouslySelected != null)
        {
            for (var i = 0; i < TemplateList.Count; i++)
            {
                if (TemplateList[i].Metadata as string != previouslySelected)
                    continue;

                TemplateList[i].Selected = true;
                break;
            }
        }

        PrintButton.Disabled = _currentTemplateId == null || _inkVolume <= 0.0f;
    }

    private readonly record struct TemplateEntry(string TemplateId, DocTemplatePrototype Template, string FullName);

    private bool TryGetTemplateEntry(string templateId, out TemplateEntry entry)
    {
        entry = default;

        if (!_prototypeManager.TryIndex<DocTemplatePrototype>(templateId, out var template))
            return false;

        entry = new TemplateEntry(templateId, template, _loc.GetString(template.Name));
        return true;
    }

    private void AddCategoryHeader(DocTemplateCategoryPrototype category, int count)
    {
        var icon = GetCategoryIconTexture(category);
        var headerText = $"{_loc.GetString(category.Name)} ({count})";
        var item = TemplateList.AddItem(headerText, icon, selectable: false);
        item.TooltipEnabled = false;

        if (icon != null)
            item.IconScale = GetIconScale(icon);
    }

    private void AddTemplateItem(string templateId, DocTemplatePrototype template, string fullName)
    {
        var icon = GetCategoryIconTexture(template.Category);
        var item = TemplateList.AddItem(
            UiTextHelper.TruncateWithEllipsis(fullName, MaxListLabelLength),
            icon,
            metadata: templateId);
        item.TooltipText = fullName;

        if (icon != null)
            item.IconScale = GetIconScale(icon);
    }

    private bool MatchesTemplateFilter(string templateId)
    {
        if (!_prototypeManager.TryIndex<DocTemplatePrototype>(templateId, out var template))
            return false;

        if (_activeCategoryFilterId is { } activeCategoryFilterId && template.Category != activeCategoryFilterId)
            return false;

        if (string.IsNullOrWhiteSpace(_searchText))
            return true;

        var templateName = _loc.GetString(template.Name).ToLowerInvariant();
        var templateIdLower = templateId.ToLowerInvariant();
        var search = _searchText.ToLowerInvariant();

        return templateName.Contains(search) || templateIdLower.Contains(search);
    }

    private List<DocTemplateCategoryPrototype> GetOrderedCategories(IEnumerable<ProtoId<DocTemplateCategoryPrototype>> categoryIds)
    {
        var categories = new List<DocTemplateCategoryPrototype>();

        foreach (var categoryId in categoryIds.Distinct())
        {
            if (_prototypeManager.TryIndex(categoryId, out DocTemplateCategoryPrototype? category))
                categories.Add(category);
        }

        categories.Sort(static (a, b) =>
        {
            var order = a.Order.CompareTo(b.Order);
            return order != 0 ? order : string.CompareOrdinal(a.ID, b.ID);
        });

        return categories;
    }

    private Texture? GetCategoryIconTexture(ProtoId<DocTemplateCategoryPrototype> categoryId)
    {
        if (!_prototypeManager.TryIndex(categoryId, out DocTemplateCategoryPrototype? category))
            return null;

        return GetCategoryIconTexture(category);
    }

    private Texture? GetCategoryIconTexture(DocTemplateCategoryPrototype category)
    {
        if (category.Icon == null)
            return null;

        if (_categoryIconTextures.TryGetValue(category.ID, out var cached))
            return cached;

        var texture = SpriteSystem.Frame0(category.Icon);
        _categoryIconTextures[category.ID] = texture;
        return texture;
    }

    private Texture? GetPrintIconTexture()
    {
        _printIconTexture ??= SpriteSystem.Frame0(new SpriteSpecifier.Rsi(CopyMachineActionsRsiPath, PrintIconState));
        return _printIconTexture;
    }

    private Texture? GetCopyIconTexture()
    {
        _copyIconTexture ??= SpriteSystem.Frame0(new SpriteSpecifier.Rsi(CopyMachineActionsRsiPath, CopyIconState));
        return _copyIconTexture;
    }

    private static float GetIconScale(Texture texture)
    {
        if (texture.Size.Y <= 0)
            return 1f;

        return Math.Clamp(ListIconTargetSize / texture.Size.Y, ListIconScaleMin, ListIconScaleMax);
    }
}
