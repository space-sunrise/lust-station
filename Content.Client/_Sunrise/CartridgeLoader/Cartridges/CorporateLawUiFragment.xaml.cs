using Content.Shared._Sunrise.CartridgeLoader.Cartridges;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client._Sunrise.CartridgeLoader.Cartridges;

[GenerateTypedNameReferences]
public sealed partial class CorporateLawUiFragment : BoxContainer
{
    private static readonly Color HeaderColor = Color.FromHex("#00d4ff");
    private static readonly Color BackgroundColor = Color.FromHex("#1a2332");
    private static readonly Color BackgroundColorDark = Color.FromHex("#141a26");

    public CorporateLawUiFragment()
    {
        RobustXamlLoader.Load(this);
        Orientation = LayoutOrientation.Vertical;
    }

    public void UpdateState(CorporateLawUiState state)
    {
        MainContainer.DisposeAllChildren();
        MainContainer.SeparationOverride = 8;

        foreach (var section in state.Sections)
        {
            var sectionColor = section.Color ?? HeaderColor;
            
            var headerStyle = new StyleBoxFlat
            {
                BackgroundColor = sectionColor.WithAlpha(0.08f),
                BorderColor = sectionColor.WithAlpha(0.4f),
                BorderThickness = new Thickness(0, 0, 0, 2),
                ContentMarginLeftOverride = 12,
                ContentMarginRightOverride = 12
            };

            var sectionHeading = new CollapsibleHeading
            {
                StyleBoxOverride = headerStyle,
                MinHeight = 36
            };
            
            sectionHeading.Label.Visible = false;
            
            var richTitle = new RichTextLabel
            {
                VerticalAlignment = Control.VAlignment.Center,
                Margin = new Thickness(4, 0, 0, 0)
            };
            richTitle.SetMessage(FormattedMessage.FromMarkupOrThrow($"[color={sectionColor.ToHex()}][bold]{section.Title}[/bold][/color]"));
            
            // Add rich label to the header container (usually a BoxContainer)
            if (sectionHeading.ChildCount > 0 && sectionHeading.GetChild(0) is BoxContainer box)
            {
                box.AddChild(richTitle);
            }

            var sectionBody = new CollapsibleBody { HorizontalExpand = true };
            var sectionContent = new BoxContainer
            {
                Orientation = LayoutOrientation.Vertical,
                HorizontalExpand = true,
                Margin = new Thickness(4, 4, 4, 8),
                SeparationOverride = 6
            };
            sectionBody.AddChild(sectionContent);

            foreach (var entry in section.Entries)
            {
                var articleHeaderStyle = new StyleBoxFlat
                {
                    BackgroundColor = sectionColor.WithAlpha(0.15f),
                    BorderColor = sectionColor.WithAlpha(0.8f),
                    BorderThickness = new Thickness(4, 0, 0, 0),
                    ContentMarginLeftOverride = 12,
                    ContentMarginRightOverride = 8
                };

                var entryHeading = new CollapsibleHeading
                {
                    StyleBoxOverride = articleHeaderStyle,
                    MinHeight = 32,
                    Margin = new Thickness(8, 2, 0, 0)
                };
                
                entryHeading.Label.Visible = false;
                
                var titleText = string.IsNullOrEmpty(entry.Identifier) 
                    ? entry.Title 
                    : $"[bold]{entry.Identifier}[/bold] {entry.Title}";
                
                var articleRichTitle = new RichTextLabel
                {
                    VerticalAlignment = Control.VAlignment.Center,
                    Margin = new Thickness(4, 0, 0, 0)
                };
                articleRichTitle.SetMessage(FormattedMessage.FromMarkupOrThrow(titleText));
                
                if (entryHeading.ChildCount > 0 && entryHeading.GetChild(0) is BoxContainer articleBox)
                {
                    articleBox.AddChild(articleRichTitle);
                }

                var entryBody = new CollapsibleBody 
                { 
                    HorizontalExpand = true,
                };
                
                var bodyStyle = new StyleBoxFlat
                {
                    BackgroundColor = sectionColor.WithAlpha(0.05f),
                    BorderColor = sectionColor.WithAlpha(0.3f),
                    BorderThickness = new Thickness(4, 0, 0, 0),
                    ContentMarginLeftOverride = 12,
                    ContentMarginBottomOverride = 8,
                    ContentMarginTopOverride = 4
                };

                var entryContentPanel = new PanelContainer
                {
                    PanelOverride = bodyStyle,
                    Margin = new Thickness(8, 0, 0, 2),
                    HorizontalExpand = true
                };
                
                var entryContent = new BoxContainer
                {
                    Orientation = LayoutOrientation.Vertical,
                    HorizontalExpand = true
                };
                
                entryContentPanel.AddChild(entryContent);
                entryBody.AddChild(entryContentPanel);

                var entryDesc = new RichTextLabel 
                { 
                    HorizontalExpand = true,
                    Margin = new Thickness(4, 4, 8, 4)
                };
                entryDesc.SetMessage(FormattedMessage.FromMarkupOrThrow(entry.Description));
                entryContent.AddChild(entryDesc);

                var entryCollapsible = new Collapsible(entryHeading, entryBody);
                sectionContent.AddChild(entryCollapsible);

                entryHeading.OnPressed += _ => entryCollapsible.BodyVisible = !entryCollapsible.BodyVisible;
            }

            var sectionCollapsible = new Collapsible(sectionHeading, sectionBody);
            MainContainer.AddChild(sectionCollapsible);

            sectionHeading.OnPressed += _ => sectionCollapsible.BodyVisible = !sectionCollapsible.BodyVisible;
        }
    }
}
