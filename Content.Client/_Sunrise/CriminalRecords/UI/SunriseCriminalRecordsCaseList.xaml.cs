using System.Linq;
using Content.Shared._Sunrise.CriminalRecords;
using Content.Shared._Sunrise.Laws;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface.Controls;
using Robust.Shared.Prototypes;

namespace Content.Client._Sunrise.CriminalRecords.UI;

// --- Case Item Control ---
[GenerateTypedNameReferences]
public sealed partial class SunriseCriminalRecordsCaseControl : Button
{
    public Action<CriminalCase>? OnEdit;

    public SunriseCriminalRecordsCaseControl()
    {
        RobustXamlLoader.Load(this);
    }

    public void Setup(CriminalCase @case, IPrototypeManager proto)
    {
        CaseIdLabel.Text = $"#{@case.Id}";

        var statusLoc = @case.Status switch
        {
            CriminalCaseStatus.Open => "sunrise-records-status-open",
            CriminalCaseStatus.Incarcerated => "sunrise-records-status-incarcerated",
            CriminalCaseStatus.Finished => "sunrise-records-status-finished",
            CriminalCaseStatus.Closed => "sunrise-records-status-closed",
            _ => "unknown"
        };
        StatusLabel.Text = $"[{Loc.GetString(statusLoc)}]";

        var statusColor = @case.Status switch
        {
            CriminalCaseStatus.Open => Color.FromHex("#ff4444"),
            CriminalCaseStatus.Incarcerated => Color.FromHex("#ffbb33"),
            CriminalCaseStatus.Finished => Color.FromHex("#00C851"),
            _ => Color.FromHex("#aaaaaa")
        };
        StatusLabel.FontColorOverride = statusColor;

        var lawCodes = @case.Laws
            .Select(id =>
            {
                if (!proto.TryIndex<CorporateLawPrototype>(id, out var law)) return id;
                return !string.IsNullOrEmpty(law.LawIdentifier) ? law.LawIdentifier : id.Replace("Law", "");
            });
        LawsSummaryLabel.Text = string.Join(", ", lawCodes);

        var circNames = @case.Circumstances
            .Select(id => proto.TryIndex<CorporateLawPrototype>(id, out var law) ? Loc.GetString(law.Title) : id);

        CircumstancesContainer.DisposeAllChildren();
        foreach (var name in circNames)
        {
            CircumstancesContainer.AddChild(new Label
            {
                Text = $"- {name}",
                StyleClasses = { "LabelSecondaryColor" },
                Margin = new Thickness(8, 0, 0, 0),
                MouseFilter = MouseFilterMode.Ignore
            });
        }

        OnPressed += _ => OnEdit?.Invoke(@case);
    }
}

// --- Case List Fragment ---
[GenerateTypedNameReferences]
public sealed partial class SunriseCriminalRecordsCaseList : Control
{
    public Action? OnCreateCase;
    public Action<CriminalCase>? OnEditCase;

    public SunriseCriminalRecordsCaseList()
    {
        RobustXamlLoader.Load(this);

        VerticalExpand = true;
        HorizontalExpand = true;

        CreateCaseButton.OnPressed += _ => OnCreateCase?.Invoke();
    }

    public void Populate(List<CriminalCase> cases, IPrototypeManager proto)
    {
        CasesContainer.DisposeAllChildren();
        foreach (var @case in cases)
        {
            var control = new SunriseCriminalRecordsCaseControl();
            control.Setup(@case, proto);
            control.OnEdit += c => OnEditCase?.Invoke(c);
            CasesContainer.AddChild(control);
        }
    }
}
