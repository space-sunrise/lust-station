using System.Linq;
using Content.Shared._Sunrise.CriminalRecords;
using Content.Shared._Sunrise.Laws.Systems;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._Sunrise.CriminalRecords.UI;

[GenerateTypedNameReferences]
public sealed partial class SunriseCriminalRecordsWindow : DefaultWindow
{
    [Dependency] private readonly IEntityManager _ent = default!;
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    private readonly SharedStationCorporateLawSystem _stationLaw;

    private SunriseCriminalRecordsConsoleBoundUserInterface? _owner;
    private SunriseCriminalRecordsConsoleState? _lastState;

    private SunriseCriminalRecordsDetailsControl? _detailsControl;
    private SunriseCriminalRecordsCaseList? _caseList;
    private SunriseCriminalRecordsCaseEditor? _caseEditor;


    public SunriseCriminalRecordsWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _stationLaw = _ent.System<SharedStationCorporateLawSystem>();

        SearchInput.OnTextChanged += args => RefreshRecordList(args.Text);
        RecordList.OnItemSelected += args =>
        {
            if (args.ItemList[args.ItemIndex].Metadata is uint recordId)
            {
                _owner?.SelectRecord(recordId);
            }
        };
    }

    public void Initialize(SunriseCriminalRecordsConsoleBoundUserInterface owner)
    {
        _owner = owner;
    }

    public void UpdateState(SunriseCriminalRecordsConsoleState state)
    {
        _lastState = state;

        RefreshRecordList(SearchInput.Text);

        if (state.SelectedStationRecord == null)
        {
            foreach (var child in MainContentContainer.Children.ToArray())
            {
                if (child != NoSelectionLabel)
                    child.Dispose();
            }
            NoSelectionLabel.Visible = true;
            _detailsControl = null;
            return;
        }

        NoSelectionLabel.Visible = false;

        if (_detailsControl == null)
        {
            _detailsControl = new SunriseCriminalRecordsDetailsControl();
            MainContentContainer.AddChild(_detailsControl);
        }

        _detailsControl.UpdateInfo(state);

        // Routing logic
        if (state.CurrentUIState == SunriseCriminalRecordsUIState.Editor)
        {
            ShowEditor(state);
        }
        else
        {
            ShowCaseList(state);
        }
    }

    private void ShowCaseList(SunriseCriminalRecordsConsoleState state)
    {
        if (_caseList == null)
        {
            _caseList = new SunriseCriminalRecordsCaseList();
            _caseList.OnCreateCase += () => _owner?.CreateCase();
            _caseList.OnEditCase += c => _owner?.SelectCase(c.Id);
        }

        _stationLaw.GetEffectiveLawset(_owner?.Owner, out _, out var articles, out _, out var threshold);

        _caseList.Populate(state.Cases, _prototypeManager, articles, threshold);
        _detailsControl?.SetContent(_caseList);
    }

    private void ShowEditor(SunriseCriminalRecordsConsoleState state)
    {
        if (_caseEditor == null)
        {
            _caseEditor = new SunriseCriminalRecordsCaseEditor();
            _caseEditor.ConsoleEntity = _owner?.Owner;
            _caseEditor.OnBack += () => _owner?.SetUIState(SunriseCriminalRecordsUIState.List);
            _caseEditor.OnSave += (id, laws, circs, notes) => _owner?.UpdateCase(id, laws, circs, notes);
        }

        // Handle case selection: prioritze user selection, then open case, then last case
        var activeCase = state.Cases.Find(c => c.Id == state.SelectedCaseId)
                         ?? state.Cases.FindLast(c => c.Status == CriminalCaseStatus.Open)
                         ?? state.Cases.LastOrDefault();

        if (activeCase != null)
        {
            _caseEditor.OnFinalize = () => _owner?.CloseCase(activeCase.Id);
            _caseEditor.Setup(activeCase, state.Cases);
        }
        else
            _caseEditor.SetupNew();


        _detailsControl?.SetContent(_caseEditor);
    }

    private void RefreshRecordList(string filter = "")
    {
        if (_lastState == null) return;

        RecordList.Clear();
        var filterLower = filter.ToLowerInvariant();

        foreach (var (id, name) in _lastState.Records)
        {
            if (!string.IsNullOrEmpty(filterLower) && !name.ToLowerInvariant().Contains(filterLower))
                continue;

            var item = RecordList.AddItem(name);
            item.Metadata = id;
        }
    }
}
