namespace Content.Client._Sunrise.CriminalRecords.UI;

[GenerateTypedNameReferences]
public sealed partial class PrisonerManagementConsoleWindow : FancyWindow
{
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    public Action<uint, uint, int>? OnStartIncarceration;
    public Action<uint, uint>? OnEscape;
    public Action<uint, uint>? OnParole;
    private Dictionary<int, bool> _cellOccupied = new();

    public PrisonerManagementConsoleWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        var columnStyle = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex("#202025"),
            BorderColor = Color.FromHex("#303035"),
            BorderThickness = new Thickness(1)
        };

        WaitingPanel.PanelOverride = columnStyle;
        InProgressPanel.PanelOverride = columnStyle;
        FinishedPanel.PanelOverride = columnStyle;
    }

    private PrisonerManagementConsoleState? _lastState;

    public void UpdateState(PrisonerManagementConsoleState state)
    {
        _lastState = state;
        _cellOccupied = state.CellOccupied;
        PopulateWaiting(state.Waiting);
        PopulateInProgress(state.InProgress);
        PopulateFinished(state.Finished);
    }

    private void PopulateWaiting(List<PrisonerRecordInfo> records)
    {
        if (_lastState == null)
            return;

        var threshold = _lastState.PermanentThreshold;

        WaitingContainer.DisposeAllChildren();
        foreach (var record in records)
        {
            var control = new PrisonerRecordControl();
            control.PermanentThreshold = threshold;
            control.SetupWaiting(record);

            if (record.Sentence >= threshold)
                control.OnActionPressed += () => OnStartIncarceration?.Invoke(record.RecordId, record.CaseId, -1);
            else
                control.OnActionPressed += () => OpenCellSelection(record);

            WaitingContainer.AddChild(control);
        }
    }

    private void PopulateInProgress(List<IncarcerationInfo> incars)
    {
        if (_lastState == null)
            return;

        var threshold = _lastState.PermanentThreshold;

        InProgressContainer.DisposeAllChildren();
        foreach (var incar in incars)
        {
            var control = new PrisonerRecordControl();
            control.PermanentThreshold = threshold;
            control.SetCellIndex(incar.CellIndex);
            control.SetupInProgress(incar);

            control.OnEscapePressed += () => OnEscape?.Invoke(incar.RecordId, incar.CaseId);
            control.OnParolePressed += () => OnParole?.Invoke(incar.RecordId, incar.CaseId);

            InProgressContainer.AddChild(control);
        }
    }

    private void PopulateFinished(List<PrisonerRecordInfo> records)
    {
        FinishedContainer.DisposeAllChildren();
        foreach (var record in records)
        {
            var control = new PrisonerRecordControl();
            control.SetupFinished(record);
            FinishedContainer.AddChild(control);
        }
    }


    private void OpenCellSelection(PrisonerRecordInfo record)
    {
        if (_lastState == null) return;

        var dialog = new CellSelectionDialog(_cellOccupied, _lastState.CellEquipped);
        dialog.OnCellSelected += cellIdx =>
        {
            OnStartIncarceration?.Invoke(record.RecordId, record.CaseId, cellIdx);
            dialog.Close();
        };
        dialog.OpenCentered();
    }
}

public sealed class CellSelectionDialog : DefaultWindow
{
    public Action<int>? OnCellSelected;

    public CellSelectionDialog(Dictionary<int, bool> occupied, Dictionary<int, bool> equipped)
    {
        Title = Loc.GetString("prisoner-management-select-cell");
        var grid = new GridContainer { Columns = 2, Margin = new Thickness(8), HSeparationOverride = 8, VSeparationOverride = 8 };
        
        // Dynamic cell count based on the number of configured ports/sinks from the server
        var cellCount = equipped.Count;
        for (int i = 0; i < cellCount; i++)
        {
            var btn = new Button { Text = Loc.GetString("prisoner-management-cell-index", ("index", i + 1)), MinWidth = 100 };

            bool isOccupied = occupied.GetValueOrDefault(i, false);
            bool isEquipped = equipped.GetValueOrDefault(i, false); // Fail-Closed: must be explicitly true

            if (isOccupied)
            {
                btn.Disabled = true;
                btn.Text += $" ({Loc.GetString("prisoner-management-occupied")})";
            }
            else if (!isEquipped)
            {
                btn.Disabled = true;
                btn.Text += $" ({Loc.GetString("prisoner-management-unequipped")})";
            }
            else
            {
                var idx = i;
                btn.OnPressed += _ => OnCellSelected?.Invoke(idx);
            }
            grid.AddChild(btn);
        }
        
        if (cellCount == 0)
        {
            grid.AddChild(new Label { Text = Loc.GetString("prisoner-management-no-cells") });
        }
        
        Contents.AddChild(grid);
    }
}
