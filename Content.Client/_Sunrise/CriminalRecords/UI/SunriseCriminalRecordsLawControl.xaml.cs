using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface.Controls;
using Robust.Shared.Input;

namespace Content.Client._Sunrise.CriminalRecords.UI;

[GenerateTypedNameReferences]
public sealed partial class SunriseCriminalRecordsLawControl : PanelContainer
{
    public Action<bool>? OnToggled;

    private StyleBox? _normalStyle;
    private StyleBox? _hoverStyle;
    private bool _isHovered;

    public SunriseCriminalRecordsLawControl()
    {
        RobustXamlLoader.Load(this);
        SelectionCheckBox.OnToggled += args => OnToggled?.Invoke(args.Pressed);

        MouseFilter = MouseFilterMode.Stop;
    }

    public void Setup(string title, bool pressed, bool disabled, Color? catColor = null)
    {
        LawTitleLabel.Text = title;
        SelectionCheckBox.Pressed = pressed;
        SelectionCheckBox.Disabled = disabled;

        if (catColor != null)
        {
            var color = catColor.Value;
            _normalStyle = new StyleBoxFlat { BackgroundColor = Color.FromHex("#1a1a1e").WithAlpha(0.4f) };
            _hoverStyle = new StyleBoxFlat { BackgroundColor = color.WithAlpha(0.1f) };

            LateralStrip.PanelOverride = new StyleBoxFlat { BackgroundColor = color };
            LawTitleLabel.FontColorOverride = color.WithAlpha(0.9f);
        }
        else
        {
            _normalStyle = null;
            _hoverStyle = null;
            LateralStrip.PanelOverride = null;
            LawTitleLabel.FontColorOverride = null;
        }

        UpdateStyles();
    }

    protected override void KeyBindDown(GUIBoundKeyEventArgs args)
    {
        base.KeyBindDown(args);
        if (SelectionCheckBox.Disabled || args.Function != EngineKeyFunctions.UIClick)
            return;

        SelectionCheckBox.Pressed = !SelectionCheckBox.Pressed;
        OnToggled?.Invoke(SelectionCheckBox.Pressed);
    }

    protected override void MouseEntered()
    {
        base.MouseEntered();
        _isHovered = true;
        UpdateStyles();
    }

    protected override void MouseExited()
    {
        base.MouseExited();
        _isHovered = false;
        UpdateStyles();
    }

    private void UpdateStyles()
    {
        if (_normalStyle == null || _hoverStyle == null)
        {
            BackgroundPanel.PanelOverride = null;
            return;
        }

        BackgroundPanel.PanelOverride = _isHovered ? _hoverStyle : _normalStyle;
    }
}
