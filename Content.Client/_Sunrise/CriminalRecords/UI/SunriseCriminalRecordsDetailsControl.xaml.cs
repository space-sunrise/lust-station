using Content.Shared._Sunrise.CriminalRecords;
using Content.Shared.Humanoid.Prototypes;
using Content.Shared.Security;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._Sunrise.CriminalRecords.UI;

[GenerateTypedNameReferences]
public sealed partial class SunriseCriminalRecordsDetailsControl : Control
{
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    public SunriseCriminalRecordsDetailsControl()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        VerticalExpand = true;
        HorizontalExpand = true;

        foreach (var status in Enum.GetValues<SecurityStatus>())
        {
            StatusOption.AddItem(Loc.GetString($"criminal-records-status-{status.ToString().ToLower()}"), (int) status);
        }

        StatusOption.OnItemSelected += args =>
        {
            OnStatusChanged?.Invoke((SecurityStatus) args.Id, StatusReasonInput.Text);
            StatusOption.SelectId(args.Id);
        };
    }

    public event Action<SecurityStatus, string?>? OnStatusChanged;

    public void UpdateInfo(SunriseCriminalRecordsConsoleState state)
    {
        PersonName.Text = state.SelectedName ?? Loc.GetString("sunrise-records-unknown-name");
        PersonJob.Text = state.JobTitle ?? Loc.GetString("sunrise-records-unknown-job");
        AgeLabel.Text = state.Age?.ToString() ?? Loc.GetString("sunrise-records-unknown-age");
        
        if (state.Gender != null)
            GenderLabel.Text = Loc.GetString("station-records-gender", ("gender", state.Gender));
        else
            GenderLabel.Text = Loc.GetString("sunrise-records-unknown-gender");

        if (state.Species != null && _prototypeManager.TryIndex<SpeciesPrototype>(state.Species, out var species))
            SpeciesLabel.Text = Loc.GetString(species.Name);
        else
            SpeciesLabel.Text = state.Species ?? Loc.GetString("sunrise-records-none");

        DnaLabel.Text = state.DNA ?? Loc.GetString("sunrise-records-none");
        FingerprintsLabel.Text = state.Fingerprints ?? Loc.GetString("sunrise-records-none");

        StatusOption.SelectId((int) state.Status);
        StatusReasonLabel.Text = state.StatusReason;
        StatusReasonLabel.Visible = !string.IsNullOrWhiteSpace(state.StatusReason);

        var isEditor = state.CurrentUIState == SunriseCriminalRecordsUIState.Editor;
        MetadataContainer.Visible = !isEditor;
        MetadataDivider.Visible = !isEditor;
        StatusContainer.Visible = !isEditor;
        StatusDivider.Visible = !isEditor;
    }

    public void SetContent(Control control)
    {
        if (ContentArea.ChildCount > 0 && ContentArea.GetChild(0) == control)
            return;

        ContentArea.RemoveAllChildren();
        ContentArea.AddChild(control);
    }
}
