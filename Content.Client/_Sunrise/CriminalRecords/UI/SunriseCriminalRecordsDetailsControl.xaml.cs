using Content.Shared._Sunrise.CriminalRecords;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Sunrise.CriminalRecords.UI;

[GenerateTypedNameReferences]
public sealed partial class SunriseCriminalRecordsDetailsControl : Control
{
    public SunriseCriminalRecordsDetailsControl()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        VerticalExpand = true;
        HorizontalExpand = true;
    }

    public void UpdateInfo(SunriseCriminalRecordsConsoleState state)
    {
        PersonName.Text = state.SelectedName ?? Loc.GetString("sunrise-records-unknown-name");
        PersonJob.Text = state.JobTitle ?? Loc.GetString("sunrise-records-unknown-job");
        AgeLabel.Text = state.Age?.ToString() ?? Loc.GetString("sunrise-records-unknown-age");
        GenderLabel.Text = state.Gender ?? Loc.GetString("sunrise-records-unknown-gender");
        DnaLabel.Text = state.DNA ?? Loc.GetString("sunrise-records-none");
        FingerprintsLabel.Text = state.Fingerprints ?? Loc.GetString("sunrise-records-none");
    }

    public void SetContent(Control control)
    {
        if (ContentArea.ChildCount > 0 && ContentArea.GetChild(0) == control)
            return;

        ContentArea.RemoveAllChildren();
        ContentArea.AddChild(control);
    }
}
